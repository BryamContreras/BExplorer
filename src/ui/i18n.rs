pub fn view_mode_label<'a>(config: &'a AppConfig, view_mode: &ViewMode) -> &'a str {
    match view_mode {
        ViewMode::Details => tr(config, "details"),
        ViewMode::List => tr(config, "list"),
        ViewMode::SmallIcons => tr(config, "small_icons"),
        ViewMode::MediumIcons => tr(config, "medium_icons"),
        ViewMode::LargeIcons => tr(config, "large_icons"),
        ViewMode::ExtraLargeIcons => tr(config, "extra_large_icons"),
        ViewMode::Tiles => tr(config, "tiles"),
    }
}

pub fn place_label(config: &AppConfig, label: &str) -> String {
    match label {
        "Home" => tr(config, "home").to_string(),
        "Desktop" => tr(config, "desktop").to_string(),
        "Downloads" => tr(config, "downloads").to_string(),
        "Documents" => tr(config, "documents").to_string(),
        "Music" => tr(config, "music").to_string(),
        "Pictures" => tr(config, "pictures").to_string(),
        "Videos" => tr(config, "videos").to_string(),
        _ => label.to_string(),
    }
}
